#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*b;
	size_t			i;

	i = 0;
	b = s;
	while (i < n)
	{
		b[i] = c;
		i++;
	}	
	return (s);
}
